package com.caberodev.squarearmy.entity;
public abstract class AbstractEntity implements Entity{

	protected float x;
	protected float y;

	public AbstractEntity(float x, float y) {
		this.x = x;
		this.y = y;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

}
